/**
 * Constants for column sizing and layout.
 */

/**
 * Default column width in pixels.
 * Matches Tailwind's w-80 class (320px).
 */
export const DEFAULT_COLUMN_WIDTH = 320;

/**
 * Minimum column width in pixels.
 * Prevents columns from becoming too narrow to use.
 */
export const MIN_COLUMN_WIDTH = 200;

/**
 * Maximum column width in pixels.
 * Prevents columns from becoming too wide.
 */
export const MAX_COLUMN_WIDTH = 480;
